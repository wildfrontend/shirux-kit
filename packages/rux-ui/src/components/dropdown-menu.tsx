/**
 * 擴充 DropdownMenu Root 的 Props，支援條件式開關控制
 */
interface DropdownMenuProps
  extends React.ComponentPropsWithoutRef<typeof DropdownMenuPrimitive.Root> {
  /**
   * 在狀態變更前執行的驗證函數
   * @param open - 即將變更的狀態
   * @returns true 允許操作，false 拒絕操作
   * @returns Promise<boolean> 支援非同步驗證
   *
   * @example
   * ```tsx
   * <DropdownMenu
   *   onBeforeOpenChange={(open) => {
   *     if (open && !isLoggedIn) {
   *       showLoginModal();
   *       return false; // 拒絕打開
   *     }
   *     return true; // 允許操作
   *   }}
   * >
   *   ...
   * </DropdownMenu>
   * ```
   */
  onBeforeOpenChange?: (open: boolean) => boolean | Promise<boolean>;
}

/**
 * 擴充版 DropdownMenu 根組件
 * 支援在狀態變更前進行條件驗證和攔截
 */
const DropdownMenuRoot: RC<DropdownMenuProps> = ({
  onBeforeOpenChange,
  onOpenChange,
  open: controlledOpen,
  ...props
}) => {
  const [uncontrolledOpen, setUncontrolledOpen] = React.useState(false);
  const isControlled = controlledOpen !== undefined;
  const open = isControlled ? controlledOpen : uncontrolledOpen;

  const handleOpenChange = React.useCallback(
    async (newOpen: boolean) => {
      // 如果有 onBeforeOpenChange，先執行驗證
      if (onBeforeOpenChange) {
        const allowed = await onBeforeOpenChange(newOpen);
        if (!allowed) {
          // 驗證失敗，拒絕操作
          return;
        }
      }

      // 驗證通過，更新狀態（僅在非受控模式）
      if (!isControlled) {
        setUncontrolledOpen(newOpen);
      }

      // 執行原本的 onOpenChange
      onOpenChange?.(newOpen);
    },
    [onBeforeOpenChange, onOpenChange, isControlled]
  );

  return (
    <DropdownMenuPrimitive.Root
      open={open}
      onOpenChange={handleOpenChange}
      {...props}
    />
  );
};

DropdownMenuRoot.displayName = 'DropdownMenu';

